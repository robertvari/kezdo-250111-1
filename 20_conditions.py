number = 10

#     True
if number == 10:
    print("The number is 10")

#     False
if number != 10:
    print("The number is not 10")

if number < 10:
    print("The number is less than 10")

if number > 10:
    print("The number is greater than 10")

if number <= 10:
    print("The number is 10 or less.")

if number >= 10:
    print("The number is greater os equal to 10")